package com.simplilearn.typecasting;

public class Home {

	// default variable
	int noOfRooms = 5;
	String homeName = "Johns Homes";
	boolean isOpen = false;

	// default method
	void showAll() {
		System.out.println("The House " + homeName + " has no of rooms "
				+ noOfRooms + " Is it open ? " + isOpen);
	}

	// default constructor
	public Home() {}
}
